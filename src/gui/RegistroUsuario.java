    private void btnRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistroActionPerformed
        // TODO add your handling code here:

        if (this.txtNombre.getText().isEmpty()
                || this.txtApellidos.getText().isEmpty()
                || this.txtCorreo.getText().isEmpty()
                || this.txtTel.getText().isEmpty()
                || this.txtEdad.getText().isEmpty()
                || this.txtUserName.getText().isEmpty()
                || this.txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor complete todos los campos", "Campos en blanco", JOptionPane.INFORMATION_MESSAGE);
        } else {
            Usuario usuarios = new Usuario();
            usuarios.setNombre(this.txtNombre.getText());
            usuarios.setApellido(this.txtApellidos.getText());
            usuarios.setCorreo(this.txtCorreo.getText());
            usuarios.setTelefono(this.txtTel.getText());

            int edad = Integer.parseInt(this.txtEdad.getText());
            usuarios.setEdad(edad);

            Usuario usuarios2 = new Usuario();
            usuarios2.setNombreUsuario(this.txtUserName.getText());
            usuarios2.setContrasenia(this.txtPassword.getText());

            Crud crud = new Crud();

            boolean resultado = crud.insertar(Sql.insertarUsuario(usuarios));
            boolean resultado2 = crud.insertar(Sql.registroUsuarioL(usuarios2));

            if (resultado && resultado2) {
                JOptionPane.showMessageDialog(null, "Usuario registrado", "Usuarios", 1);
            } else {
                JOptionPane.showMessageDialog(null, "Ocurrio un error", "Error", 2);
            }

        }

        if (evt.getSource() == this.btnRegistro) {
            this.txtNombre.setText("");
            this.txtPassword.setText("");
            this.txtApellidos.setText("");
            this.txtCorreo.setText("");
            this.txtTel.setText("");
            this.txtEdad.setText("");
            this.txtUserName.setText("");
            this.txtPassword.setText("");

        }


    }//GEN-LAST:event_btnRegistroActionPerformed

