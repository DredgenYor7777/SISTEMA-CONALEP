    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        // TODO add your handling code here:

        char c = evt.getKeyChar();
        if (Character.isDigit(c)) {
            evt.consume();
            Toolkit.getDefaultToolkit().beep();

            JOptionPane.showMessageDialog(null, "No puede ingresar n√∫meros", "Tipo de entrada", JOptionPane.WARNING_MESSAGE);

        }

        if (this.txtNombre.getText().length() >= 30) {
            evt.consume();
            Toolkit.getDefaultToolkit().beep();
            JOptionPane.showMessageDialog(null, "Limite de caracteres alcanzado", "Longitud nombre", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_txtNombreKeyTyped

